package org.firstinspires.ftc.team10309.API;

/**
 * A class representing a 2D vector
 */
public class Vec2 {

    /**
     * The X component of a 2D vector
     */
    public float x;
    /**
     * The Y component of a 2D vector
     */
    public float y;

    /**
     * Vec2 constructor
     * @param x The value to be assigned to the x component
     * @param y The value to be assigned to the y component
     */
    public Vec2(float x, float y) {
        this.x = x;
        this.y = y;
    }
}
